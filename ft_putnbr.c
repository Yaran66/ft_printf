#include "ft_printf.h"

void	ft_putnbr(long i)
{
	if (i < 0 )
	{
		ft_putchar ('-');
		i = i * (-1);
	}
	if (i < 10)
		ft_putchar ((char)('0' + i));
	if (i > 9)
	{
		ft_putnbr (i / 10);
		ft_putchar ((char)('0' + (i % 10)));
	}
}
